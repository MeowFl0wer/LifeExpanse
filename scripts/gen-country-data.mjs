/**
 * Generates src/lib/countryData.ts — the static ISO 3166-1 lookup table.
 *
 * The map (world-atlas) indexes countries by numeric code; the database stores
 * alpha-3 because `CHN` is legible where `156` is not. This table bridges the
 * two and carries the Chinese and English display names.
 *
 * Committed as generated output so there is no runtime dependency on
 * i18n-iso-countries — regenerate with `node scripts/gen-country-data.mjs`
 * when the ISO data updates.
 */
import { createRequire } from 'node:module'
import { writeFileSync } from 'node:fs'

const require = createRequire(import.meta.url)
const iso = require('i18n-iso-countries')
iso.registerLocale(require('i18n-iso-countries/langs/zh.json'))
iso.registerLocale(require('i18n-iso-countries/langs/en.json'))

const rows = Object.keys(iso.getAlpha3Codes())
  .map(alpha3 => ({
    alpha3,
    numeric: iso.alpha3ToNumeric(alpha3),
    zh: iso.getName(alpha3, 'zh') ?? iso.getName(alpha3, 'en') ?? alpha3,
    en: iso.getName(alpha3, 'en') ?? alpha3,
  }))
  .sort((a, b) => a.alpha3.localeCompare(b.alpha3))

const body = rows
  .map(r => `  ['${r.alpha3}', { numeric: '${r.numeric}', zh: '${r.zh.replace(/'/g, "\\'")}', en: '${r.en.replace(/'/g, "\\'")}' }],`)
  .join('\n')

const out = `// GENERATED by scripts/gen-country-data.mjs — do not edit by hand.
// Source: i18n-iso-countries (ISO 3166-1). Regenerate when the standard updates.

export interface CountryInfo {
  /** ISO 3166-1 numeric, as world-atlas TopoJSON indexes its geometries. */
  numeric: string
  /** Chinese display name. */
  zh: string
  /** English display name. */
  en: string
}

/** alpha-3 → country info. ${rows.length} entries. */
export const COUNTRY_DATA: ReadonlyMap<string, CountryInfo> = new Map([
${body}
])
`
writeFileSync(new URL('../src/lib/countryData.ts', import.meta.url), out)
console.log(`wrote ${rows.length} countries to src/lib/countryData.ts`)
